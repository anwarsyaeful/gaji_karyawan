    public void tampil(){
        gj = gc.getAll();
            Object data [][] = new Object[gj.size()][9];
            int x = 0;
            for (Gaji g : gj) {
                data[x][0] = g.getNrp();
                data[x][1] = g.getNama();
                data[x][2] = g.getJabatan();
                data[x][3] = NumberFormat.getNumberInstance().format((g.getGapok()));
                data[x][4] = NumberFormat.getNumberInstance().format((g.getTunjangan()));
                data[x][5] = NumberFormat.getNumberInstance().format((g.getTransport()));
                data[x][6] = g.getMasuk();
                data[x][7] = NumberFormat.getNumberInstance().format((g.getGaji()));
                ++x;
            }
            String[] judul = {"nrp","nama","jabatan","gapok","tunjangan","transport","masuk","gaji"};
            table.setModel(new DefaultTableModel(data,judul));
    }
    
