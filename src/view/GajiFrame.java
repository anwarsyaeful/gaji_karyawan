    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        gj = gc.searching(txtCari.getText().toString());
        Object data [][] = new Object[gj.size()][9];
            int x = 0;
            for (Gaji g : gj) {
                data[x][0] = g.getNrp();
                data[x][1] = g.getNama();
                data[x][2] = g.getJabatan();
                data[x][3] = g.getGapok();
                data[x][4] = g.getTunjangan();
                data[x][5] = g.getTransport();
                data[x][6] = g.getMasuk();
                data[x][7] = g.getGaji();
                ++x;
            }
            String[] judul = {"nrp","nama","jabatan","gapok","tunjangan","transport","masuk","gaji"};
            table.setModel(new DefaultTableModel(data,judul));
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        // TODO add your handling code here:
        java.sql.Connection conn = new DBConnection().connect();
        try{
            String namafile ="src/report/gaji.jasper";
            File report = new File(namafile);
            JasperReport jreprt = (JasperReport) JRLoader.loadObject(report.getPath());
            JasperPrint jprintt = JasperFillManager.fillReport(jreprt,null,conn);
            JasperViewer.viewReport(jprintt, false);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "gagal membuka laporan","cetak laporan",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCetakActionPerformed

