    public void loadGaji(){
        jabatan = ""+cmbJab.getSelectedItem();
        switch(jabatan){
            case "Manager":
                gapok = 5000000;
                break;
            case "Asisten Manager":
                gapok = 4500000;
                break;
            case "HRD":
                gapok = 4000000;
                break;
            case "Staf Keuangan":
                gapok = 3500000;
                break;
            case "Karyawan":
                gapok = 3000000;
                break;
            case "Office Boy":
                gapok = 2500000;
                break;
        }
        
        tunjangan = (int) (gapok * 0.075);
        transport = (int)(21500);
        txtGapok.setText(""+gapok);
        txtTunjangan.setText(""+tunjangan);
        txtTransport.setText(""+transport);
          
    }
    
